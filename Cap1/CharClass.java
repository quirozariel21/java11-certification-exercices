public class CharClass {
	
	public static void main(String args[]) {
		String cad = "01234567890";
		// The index of the first character is 0
		System.out.println("Option 1: " + cad.charAt(0)); // print 1
		// can take a char value as an argument.
		System.out.println("Option 2: " + cad.charAt('7')); // print 
	}
}